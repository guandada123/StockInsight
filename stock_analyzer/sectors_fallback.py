# ── 代码 → 板块名称 反向映射 ─────────────────────
CODE_TO_SECTOR: dict[str, list[str]] = {}
for _sector_name, _sector_info in SECTOR_STOCKS_FALLBACK.items():
    for _code in _sector_info.get("成分股", []):
        if _code not in CODE_TO_SECTOR:
            CODE_TO_SECTOR[_code] = []
        CODE_TO_SECTOR[_code].append(_sector_name)


def get_sector_for_code(code: str) -> str:
    """根据股票代码获取所属板块名称（支持多板块，逗号分隔）"""
    sectors = CODE_TO_SECTOR.get(code)
    if sectors:
        return "、".join(sectors)
    return "其他"
